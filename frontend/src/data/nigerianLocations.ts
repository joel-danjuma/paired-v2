// Flatten all cities for search functionality
export const ALL_NIGERIAN_CITIES: string[] = NIGERIAN_STATES.reduce((cities, state) => {
  return [...cities, ...state.cities];
}, [] as string[]);

// Get cities by state name
export const getCitiesByState = (stateName: string): string[] => {
  const state = NIGERIAN_STATES.find(s => s.name.toLowerCase() === stateName.toLowerCase());
  return state ? state.cities : [];
};

// Search locations by query
export const searchNigerianLocations = (query: string): string[] => {
  if (!query || query.length < 2) return [];
  
  const searchTerm = query.toLowerCase();
  const matchedCities = ALL_NIGERIAN_CITIES.filter(city => 
    city.toLowerCase().includes(searchTerm)
  );
  
  const matchedStates = NIGERIAN_STATES.filter(state => 
    state.name.toLowerCase().includes(searchTerm)
  ).map(state => state.name);
  
  // Combine and deduplicate
  return [...new Set([...matchedStates, ...matchedCities])];
};

// Get formatted location string (City, State)
export const getFormattedLocation = (city: string): string => {
  for (const state of NIGERIAN_STATES) {
    if (state.cities.some(c => c.toLowerCase() === city.toLowerCase())) {
      return `${city}, ${state.name}`;
    }
  }
  return city; // Return as-is if state not found
};

// Extract just the state names for location filters
export const NIGERIAN_STATE_NAMES: string[] = NIGERIAN_STATES.map(state => state.name);

